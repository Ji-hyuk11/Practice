package variable;

public class Var3 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 10; // 1. 변수 초기화 a에 10이 들어가있음
        System.out.println(a); // 2. 변수 a의 값을 읽는다. 10이 들어있다. 10을 출력한다.
        a = 50; // 3. 변수의 값을 변경, 6열에 10으로 초기화했으나 8열에서 50으로 바꿔줌
        System.out.println(a); // 4. 변수 a의 값을 읽는다. 50이 들어있다. 50을 출력한다.
    }
}
