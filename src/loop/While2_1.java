package loop;

public class While2_1 {

    public static void main(String[] args) {

        int sum = 0;
        sum = sum + 1;
        System.out.println("int=0 + sum=1" + " = " +sum);
        sum = sum + 2;
        System.out.println("int=1 + sum=2" + " = " +sum);
        sum = sum + 3;
        System.out.println("int=3 + sum=3" + " = " +sum);
    }
}
